package com.biz.hello;

public class VarBoolean_03 {
	public static void main(String[] args) {
		
		System.out.println("false AND false = "  
		                 + (false && false));
		System.out.println("false AND false = "  
                + (false && true));
		System.out.println("false AND false = "  
                + (true && false));
		System.out.println("false AND false = "  
                + (true && true));
		System.out.println("=================");
				
		
		System.out.println("false AND false = "  
		                 + (false || false));
		System.out.println("false AND false = "  
               + (false || true));
		System.out.println("false AND false = "  
               + (true || false));
		System.out.println("false AND false = "  
               + (true || true));
               
	
		
	
		
	}

}
