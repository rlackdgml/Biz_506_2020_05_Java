package com.biz.classes.model;

public class BankVO {

	private String num;
	private String date;
	private int input;
	private int ouput;
	private int valance;
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getInput() {
		return input;
	}
	public void setInput(int input) {
		this.input = input;
	}
	public int getOuput() {
		return ouput;
	}
	public void setOuput(int ouput) {
		this.ouput = ouput;
	}
	public int getValance() {
		return valance;
	}
	public void setValance(int valance) {
		this.valance = valance;
	}

	
}
