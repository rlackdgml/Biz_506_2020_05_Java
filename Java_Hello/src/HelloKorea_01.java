// Key word는 eclipse에서 볼드체로 보여준다.
// Key word사이는 반드시 빈칸을 1칸 이상 추가한다.
// Don(.)으로 연결된 Key word는 보통 빈칸을 넣지 않는다.
// key word는 대소문자를 분명히 정확히 구별하여 작성해야 한다.
// 문장 시작부분에 (//)Slash가 2개 연속으로 오면
// 그 문장은 공백과 똑같이 인식을하고 컴파일러가 무시한다.
// 이러한 문장을 주석(Commnet)라고한다.
// 코드에 대한 설명 등을 추가할때 사용한다.


// java 소스코드는 반드시 class라는 명령어가 포함된
// 명령문이 제일 먼저 나타나야 한다.
public class HelloKorea_01 {
	// launch, 시작점 이라고 하며
	// java 코드를 컴파일 한 후  실행을 했을떄
	// 반드시 있어야 하는 코드
	public static void main(String[] args) {
		
		// 숫자 30과40을 덧셈하여 Console에 보여 달라는 명령문
		System.out.println(30 + 40); // 주석
		
	} // end main
		

} // end class
